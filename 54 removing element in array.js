// removing element from an array
const number=[1,2,3,4,5];
// push
console.log(number.push(3));
console.log(number);
// push opposite pop
number.pop()
console.log(number);
// unshift opposite shift
number.shift()
console.log(number);
// splice
number.splice(2,1);
console.log(number);
number.splice(2,2);
console.log(number);